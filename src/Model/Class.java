package Model;

public class Class {
    private int class_id;
    private String teacher;
    private String teacher_head;
    private String class_title;
    private String outline;
    private int unit_count;
    private int student_count;
    private int release_status;
    private String cover_address;
    private int class_type;

    public int getClass_id() {
        return class_id;
    }

    public void setClass_id(int class_id) {
        this.class_id = class_id;
    }

    public String getTeacher() {
        return teacher;
    }

    public void setTeacher(String teacher) {
        this.teacher = teacher;
    }

    public String getTeacher_head() {
        return teacher_head;
    }

    public void setTeacher_head(String teacher_head) {
        this.teacher_head = teacher_head;
    }

    public String getClass_title() {
        return class_title;
    }

    public void setClass_title(String class_title) {
        this.class_title = class_title;
    }

    public String getOutline() {
        return outline;
    }

    public void setOutline(String outline) {
        this.outline = outline;
    }

    public int getUnit_count() {
        return unit_count;
    }

    public void setUnit_count(int unit_count) {
        this.unit_count = unit_count;
    }


    public int getStudent_count() {
        return student_count;
    }

    public void setStudent_count(int student_count) {
        this.student_count = student_count;
    }

    public int getRelease_status() {
        return release_status;
    }

    public void setRelease_status(int release_status) {
        this.release_status = release_status;
    }

    public String getCover_address() {
        return cover_address;
    }

    public void setCover_address(String cover_address) {
        this.cover_address = cover_address;
    }

    public int getClass_type() {
        return class_type;
    }

    public void setClass_type(int class_type) {
        this.class_type = class_type;
    }
}
